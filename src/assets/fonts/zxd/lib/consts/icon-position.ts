/**
 * Icon positions
 */
export enum ZIconPosition {
  /**
   * Icon is positioned at the bottom of the button
   */
  Bottom = 'bottom',

  /**
   * Icon is positioned at the left of the button
   */
  Left = 'left',

  /**
   * Icon has no position set
   */
  None = '',

  /**
   * Icon is positioned at the right of the button
   */
  Right = 'right',

  /**
   * Icon is positioned at the top of the button
   */
  Top = 'top',
}
